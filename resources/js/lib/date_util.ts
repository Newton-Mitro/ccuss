function formatDate(value?: string | null) {
    if (!value) return '—';

    return new Intl.DateTimeFormat('en-US', {
        dateStyle: 'medium',
    }).format(new Date(value));
}

function formatDateTime(value?: string | null) {
    if (!value) return '—';

    return new Intl.DateTimeFormat('en-US', {
        dateStyle: 'medium',
        timeStyle: 'short',
    }).format(new Date(value));
}

export { formatDate, formatDateTime };
